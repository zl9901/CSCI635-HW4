def calculate(data):
    res=0.0
    for j in range(len(data)):
        for k in range(len(data)):
            res+=t[j][k]*data[j]*data[k]
    return mp.exp(res)


def isling_step(data):

    for _ in range(99):
        """
        we calculate numerator first, add up all the possible combinations
        """
        for i in range(len(data)):

            """
            for calculation of denominator
            for each xi of the input data, we should calculate every situation which xi is 0 or 1
            and then get the sum of these two situations as the denominator
            """
            original = calculate(data)
            new_data = np.copy(data)
            new_data[i] = 0.0 if data[i] == 1.0 else 1.0
            altered = calculate(new_data)
            denominator = mp.mpf(original) + mp.mpf(altered)

            """
            for i-th position we set the value of this position as 0 and then calculate numerator
            """
            tmp=np.copy(data)
            tmp[i]=0.0
            numerator=calculate(tmp)

            """
            calculate the probability
            """
            prob=mp.mpf(numerator)/mp.mpf(denominator)
            #print(prob)

            """
            determine whether value of the i-th position is 0 or 1
            """
            ref = np.random.uniform(0, 1)
            data[i]=0.0 if ref<prob else 1.0
        # print this can show us the learning process
        # print(data)
    return data


